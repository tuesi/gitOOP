    private void SkipTimeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SkipTimeButtonActionPerformed
        // TODO add your handling code here:
        TimeSkipper skipper = new TimeSkipper(systemTime);
        String debt = String.valueOf(thisuser.getPrice());
        if(DayRadio.isSelected())
        {
            skipper.skipDays(skipD);
            thisuser.updateDebt(skipper.getDayDifference());
            jLabelDebt.setText(debt);
            jLabelDate.setText(sdf.format(skipper.getTime()).toString());
            skipD++;
        }
        if(WeekRadio.isSelected())
        {
            skipper.skipWeeks(skipW);
            thisuser.updateDebt(skipper.getDayDifference());
            jLabelDebt.setText(debt);
            jLabelDate.setText(sdf.format(skipper.getTime()).toString());
            skipW++;
        }
        if(MonthRadio.isSelected())
        {
            skipper.skipMonths(skipM);
            thisuser.updateDebt(skipper.getDayDifference());
            jLabelDebt.setText(debt);
            jLabelDate.setText(sdf.format(skipper.getTime()).toString());
            skipM++;
        }
    }//GEN-LAST:event_SkipTimeButtonActionPerformed

