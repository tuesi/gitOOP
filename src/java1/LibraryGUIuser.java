    private void TakenBooks(User user)
    {  
        TakenList.setModel(DLM);
        int bookCode = 0;
        for (TakenBook book : user.getBooks()) {
            DLM.addElement(book.getIsbn() + " " + sdf.format(book.getTakenAt()));
            bookCode++;
        }
        
    }
    
    private void returnBook(User user)
    {
        user.returnBook(ListIndex);
        DLM.remove(ListIndex);
    }
    
